# we can modify strings with some methods

# make upper case from strings
x = "hai"
print(x.upper())

# or make lower case
y = "INI ALMA, KAMU MENGINGATNYA?"
print(y.lower())

# removed whitespace
z = " kamu lupa?? "
print(z.strip())

# or replace strings
a = "padahal kita tidak mengenal dulunya"
print(a.replace("tidak", "saling"))

b = "iya mungkin waktu terlalu cepat berlalu"
print(b)

