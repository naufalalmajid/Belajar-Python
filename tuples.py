# tuples is data types, which it is ordered but unchangeable
# for using tuples it is like list but we using round brackets
x = ("alma", "selalu", "bersemangat", "meskipun", 2023, "hampir", "selesai")
print(x)

# tuple are ordered, unchangeable, and allowing for duplicates value
# we can check length of tuple
print(len(x))

# tuple for one item, using commas
y = ("learning",)
print(type(y))

# and tuple can filled with any type data like int, string, and bool
z = ("jam", 12, "malam", "sedang", "belajar", True)
print(type(z))

# use tuple construstor, tuple()
xyz = tuple((z))
print(z, xyz)
